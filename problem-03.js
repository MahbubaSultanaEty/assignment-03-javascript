function finalScore(omr) {
    let forRight = 0;
    let forWrong = 0;
    for (let prop in omr) {
        //  console.log(omr[prop]);
        if (prop === "right") {
             forRight = omr[prop]* 1; 
        }
        if (prop === "wrong") {
            forWrong =omr[prop]* -0.5;
        }
        if (prop === "skip") {
            continue;
        }
    }
    let questions = Object.values(omr);
    let sum = 0;
    for (let question of questions) {
        sum += question;
    }
    if (sum !== 100) {
        return "Invalid"
    }
    let total = forRight + forWrong;
    return Math.round(total);
}

